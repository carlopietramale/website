        <h4>
OWNGLOW - A WALK TO REMEMBER
        </h4>
        
        <p>
Hospital Records, 2016
        </p>
        <br>
        <p>Ownglow’s journey began back in the hot deserts of Flagstaff, Arizona where the breathtaking San Francisco Peaks first inspired his debut EP ‘A Walk To Remember’. By infusing any sound around him Ownglow’s style breathes a rare and raw quality into drum & bass from snares of forest sticks, white noise of high school hallways, crashing waves of San Diego beaches and samples of a Six Flags rollercoaster.</p>
<br>
        <p>The EP follows Ownglow on his path so far. A gorgeous fluttering vocal in ‘Angels Sing’ reflects the beautiful views of the Pacific Surfliner journey that he took while writing the track. All the way to ‘The Night Is Still Young’ pieced together - with another exciting young talent Blake - after a night out at one of east London’s drum bass residencies.</p>
<br>
<p>Ownglow’s ambition left him to pick up and leave home at 15. While sofa-surfing around LA he realized his love for electronic music and the dream of working with Hospital Records. Since then he’s immersed himself in the world of drum bass making a huge move to the UK.</p>
